// Javascript animation for Podcast button when hovered over and mouse leaves element.
function podOver(obj) {
    obj.innerHTML = "Click Me"
    obj.style.backgroundColor="#D94A38"
}
function podOut(obj) {
    obj.innerHTML = "Podcasts"
    obj.style.backgroundColor="orange"
}
